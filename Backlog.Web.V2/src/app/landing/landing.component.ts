let template = require("./landing.component.html");
let styles = require("./landing.component.scss");

export class LandingComponent extends HTMLElement {
    constructor() {
        super();
    }

    static get observedAttributes () {
        return [];
    }

    connectedCallback() {
        this.innerHTML = `<style>${styles}</style> ${template}`; 
    }

    disconnectedCallback() {

    }
    
    attributeChangedCallback (name, oldValue, newValue) {
        switch (name) {
            default:
                break;
        }
    }
}

document.addEventListener("DOMContentLoaded",() => window.customElements.define(`ce-landing`,LandingComponent));
